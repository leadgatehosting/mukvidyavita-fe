let currentLang = 'en';

function toggleLanguage() {
    currentLang = currentLang === 'en' ? 'mr' : 'en';
    applyLanguage(currentLang);
}

function applyLanguage(lang) {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            if (key === 'served_count' && el.getAttribute('data-number')) {
                // For counter, we might need to handle it differently if it's animated
                // But simply setting textContent works for static view. 
                // If animateNumber is used, it might overwrite. 
                // For Marathi numbers, animation might be tricky.
                // Let's just set textContent for now.
                el.textContent = translations[lang][key];
            } else {
                el.textContent = translations[lang][key];
            }
        }
    });

    // Update button text
    const btn = document.getElementById('lang-toggle-btn');
    if (btn) {
        btn.textContent = translations[lang]['lang_switch'];
    }
}

document.addEventListener('DOMContentLoaded', () => {
    // Check if we need to insert the button
    const nav = document.querySelector('.navbar-nav.ml-auto');
    if (nav) {
        const li = document.createElement('li');
        li.className = 'nav-item cta';
        li.innerHTML = '<a href="#" class="nav-link" id="lang-toggle-btn" onclick="toggleLanguage(); return false;">मराठी</a>';
        nav.appendChild(li);
    }
});
